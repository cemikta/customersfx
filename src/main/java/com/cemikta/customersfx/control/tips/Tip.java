package com.cemikta.customersfx.control.tips;

import com.cemikta.customersfx.control.TipOfTheDay;

/**
 * Tip model for {@link TipOfTheDay}
 *
 * @see TipOfTheDay
 *
 * @author Cem Ikta
 */
public class Tip {

    private String name;
    private String tip;
    private String imagePath;

    public Tip(String name, String tip, String imagePath) {
        this.name = name;
        this.tip = tip;
        this.imagePath = imagePath;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTip() {
        return tip;
    }

    public void setTip(String tip) {
        this.tip = tip;
    }

    public String getImagePath() {
        return imagePath;
    }

    public void setImagePath(String imagePath) {
        this.imagePath = imagePath;
    }
}
